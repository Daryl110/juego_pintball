package Model;

/**
 *
 * @author nick_
 */
public class Ball {

    private int width;
    private int height;
    private int posX = 50;
    private int posY = 50;

    public Ball(int ancho, int alto) {
        this.width = ancho;
        this.height = alto;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }
    
    

}
